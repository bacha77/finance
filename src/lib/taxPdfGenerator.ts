import jsPDF from 'jspdf';

// ── Helpers ─────────────────────────────────────────────────────────────────
const fmt = (n: number) =>
    new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', minimumFractionDigits: 2 }).format(n);

const calcTaxes = (gross: number, isEmployee: boolean) => {
    if (!isEmployee) return { gross, ss: 0, medicare: 0, federal: 0, withholding: 0, net: gross };
    const ss = gross * 0.062;
    const medicare = gross * 0.0145;
    const federal = gross * 0.12;
    const withholding = ss + medicare + federal;
    return { gross, ss, medicare, federal, withholding, net: gross - withholding };
};

const TAX_YEAR = '2025';

// ── Base doc setup ───────────────────────────────────────────────────────────
function baseDoc(churchName: string, ein: string) {
    const doc = new jsPDF({ unit: 'pt', format: 'letter' });
    doc.setFont('helvetica');
    return { doc, churchName: churchName || 'Your Church', ein: ein || 'XX-XXXXXXX' };
}

function drawBox(doc: jsPDF, x: number, y: number, w: number, h: number, label?: string, value?: string, labelSize = 7, valueSize = 10) {
    doc.setDrawColor(180, 180, 180);
    doc.rect(x, y, w, h);
    if (label) {
        doc.setFontSize(labelSize);
        doc.setTextColor(100, 100, 100);
        doc.text(label, x + 4, y + 10);
    }
    if (value) {
        doc.setFontSize(valueSize);
        doc.setTextColor(0, 0, 0);
        doc.text(value, x + 4, y + h - 6);
    }
}

function pageHeader(doc: jsPDF, title: string, subtitle: string, year: string, logoUrl?: string) {
    doc.setFillColor(10, 36, 90);
    doc.rect(0, 0, 612, 52, 'F');
    doc.setFontSize(16);
    doc.setTextColor(255, 255, 255);
    doc.setFont('helvetica', 'bold');
    doc.text(title, 40, 24);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.text(subtitle, 40, 38);
    
    if (logoUrl && logoUrl.startsWith('data:image')) {
        try {
            // Draw a white circle background for the logo
            doc.setFillColor(255, 255, 255);
            doc.circle(560, 26, 20, 'F');
            doc.addImage(logoUrl, 'PNG', 545, 11, 30, 30);
        } catch (e) {
            console.error('PDF Logo Error:', e);
        }
    }

    doc.setFontSize(12);
    doc.setTextColor(255, 200, 80);
    doc.text(`Tax Year ${year}`, 520, 28, { align: 'right' });
    doc.setTextColor(0, 0, 0);
}

function footer(doc: jsPDF, note: string) {
    doc.setFontSize(7);
    doc.setTextColor(130, 130, 130);
    doc.text(`⚠ This is a worksheet generated by Storehouse Finance. File official IRS forms at irs.gov. ${note}`, 40, 770);
    doc.text(`Generated: ${new Date().toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' })}`, 40, 782);
}

// ── W-2 Generator ────────────────────────────────────────────────────────────
export function generateW2(employee: { name: string; role: string; salary: number }, church: { name: string; ein: string; address?: string; logo_url?: string }) {
    const { doc, churchName, ein } = baseDoc(church.name, church.ein);
    const taxes = calcTaxes(employee.salary * 12, true); // Annualize monthly salary
    const addr = church.address || '123 Church Street, City, ST 00000';

    pageHeader(doc, 'Form W-2', 'Wage and Tax Statement', TAX_YEAR, church.logo_url);

    // Employee / Employer boxes
    let y = 65;
    doc.setFontSize(9); doc.setTextColor(30, 30, 30); doc.setFont('helvetica', 'bold');
    doc.text('EMPLOYER INFORMATION', 40, y); doc.setFont('helvetica', 'normal');

    y += 12;
    drawBox(doc, 40, y, 250, 38, 'a  Employer EIN', ein);
    drawBox(doc, 295, y, 270, 38, 'b  Employer Name', churchName);
    y += 44;
    drawBox(doc, 40, y, 525, 30, 'c  Employer Address', addr);

    y += 40;
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9);
    doc.text('EMPLOYEE INFORMATION', 40, y); doc.setFont('helvetica', 'normal');

    y += 12;
    drawBox(doc, 40, y, 250, 38, 'e  Employee Name', employee.name);
    drawBox(doc, 295, y, 270, 38, 'f  Employee Role', employee.role);
    y += 50;

    // Wage Boxes
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9);
    doc.text('WAGE & TAX INFORMATION', 40, y); doc.setFont('helvetica', 'normal');
    y += 12;

    const boxes: [string, string][] = [
        ['1  Wages, Tips, Other Comp', fmt(taxes.gross)],
        ['2  Federal Income Tax Withheld', fmt(taxes.federal)],
        ['3  Social Security Wages', fmt(taxes.gross)],
        ['4  Social Security Tax Withheld', fmt(taxes.ss)],
        ['5  Medicare Wages and Tips', fmt(taxes.gross)],
        ['6  Medicare Tax Withheld', fmt(taxes.medicare)],
        ['12 See Instructions for Box 12', ''],
        ['16 State Wages, Tips, etc.', fmt(taxes.gross)],
        ['17 State Income Tax', fmt(taxes.gross * 0.05)],
        ['18 Local Wages', fmt(taxes.gross)],
    ];

    let col = 0;
    boxes.forEach(([label, value], i) => {
        const x = 40 + (col % 2) * 265;
        if (i > 0 && i % 2 === 0) y += 48;
        drawBox(doc, x, y, 258, 42, label, value);
        col++;
    });

    y += 55;

    // Net summary block
    doc.setFillColor(245, 247, 250);
    doc.rect(40, y, 525, 70, 'F');
    doc.setDrawColor(200, 200, 200);
    doc.rect(40, y, 525, 70);

    doc.setFont('helvetica', 'bold'); doc.setFontSize(10); doc.setTextColor(10, 36, 90);
    doc.text('ANNUAL COMPENSATION SUMMARY', 52, y + 17);
    doc.setFont('helvetica', 'normal'); doc.setFontSize(9); doc.setTextColor(40, 40, 40);

    const sumItems = [
        ['Gross Annual Wages:', fmt(taxes.gross)],
        ['Total Withholding:', fmt(taxes.withholding)],
        ['Net Pay (Estimated):', fmt(taxes.net)],
    ];
    sumItems.forEach(([label, val], i) => {
        doc.text(label, 52 + i * 170, y + 38);
        doc.setFont('helvetica', 'bold');
        doc.text(val, 52 + i * 170, y + 52);
        doc.setFont('helvetica', 'normal');
    });

    footer(doc, 'Required by the IRS by Jan 31 each year.');
    doc.save(`W2_${TAX_YEAR}_${employee.name.replace(/\s+/g, '_')}.pdf`);
}

