// ── Form 941 Generator ───────────────────────────────────────────────────────
export function generate941(staff: any[], church: { name: string; ein: string; logo_url?: string }, quarter: number) {
    const { doc, churchName, ein } = baseDoc(church.name, church.ein);
    const qNames = ['', 'January – March', 'April – June', 'July – September', 'October – December'];

    pageHeader(doc, "Form 941", "Employer's Quarterly Federal Tax Return", TAX_YEAR, church.logo_url);

    let y = 70;
    doc.setFontSize(9); doc.setTextColor(40, 40, 40);
    drawBox(doc, 40, y, 350, 32, 'Employer Name', churchName);
    drawBox(doc, 395, y, 170, 32, 'EIN', ein);
    y += 40;
    drawBox(doc, 40, y, 525, 28, 'Quarter', `Q${quarter} — ${qNames[quarter]} ${TAX_YEAR}`);
    y += 40;

    const employees = staff.filter((s: any) => s.type === 'Full-time' || s.type === 'Part-time');
    const quarterlyGross = employees.reduce((sum: number, s: any) => sum + (s.salary * 3), 0);
    const quarterlyFed = quarterlyGross * 0.12;
    const quarterlySSEmp = quarterlyGross * 0.062;
    const quarterlySSEmr = quarterlyGross * 0.062;
    const quarterlyMedEmp = quarterlyGross * 0.0145;
    const quarterlyMedEmr = quarterlyGross * 0.0145;
    const totalLiability = quarterlyFed + quarterlySSEmp + quarterlySSEmr + quarterlyMedEmp + quarterlyMedEmr;

    const lines: [string, string][] = [
        ['Line 1  — Number of employees who received wages', `${employees.length}`],
        ['Line 2  — Wages, tips, and other compensation', fmt(quarterlyGross)],
        ['Line 3  — Federal income tax withheld from wages', fmt(quarterlyFed)],
        ['Line 5a — Taxable SS wages × 12.4%', fmt(quarterlySSEmp + quarterlySSEmr)],
        ['Line 5c — Taxable Medicare wages × 2.9%', fmt(quarterlyMedEmp + quarterlyMedEmr)],
        ['Line 6  — Total taxes before adjustments', fmt(quarterlyFed + quarterlySSEmp + quarterlySSEmr + quarterlyMedEmp + quarterlyMedEmr)],
        ['Line 12 — Total taxes after adjustments', fmt(totalLiability)],
        ['Line 14 — Balance due', fmt(Math.max(0, totalLiability - quarterlyFed))],
    ];

    lines.forEach(([label, value]) => {
        drawBox(doc, 40, y, 525, 36, label, value);
        y += 42;
    });

    y += 6;
    doc.setFillColor(240, 248, 255);
    doc.rect(40, y, 525, 44, 'F');
    doc.setDrawColor(37, 99, 235);
    doc.rect(40, y, 525, 44);
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9); doc.setTextColor(10, 36, 90);
    doc.text(`Total Deposit Liability This Quarter:  ${fmt(totalLiability)}`, 52, y + 17);
    doc.setFont('helvetica', 'normal'); doc.setFontSize(8); doc.setTextColor(40, 80, 140);
    doc.text(`Due by: ${['', 'April 30', 'July 31', 'October 31', 'January 31'][quarter]}, ${quarter === 4 ? parseInt(TAX_YEAR) + 1 : TAX_YEAR}`, 52, y + 32);

    footer(doc, `Q${quarter} return due by the last day of the month after the quarter ends.`);
    doc.save(`Form941_${TAX_YEAR}_Q${quarter}.pdf`);
}

// ── W-3 Generator ────────────────────────────────────────────────────────────
export function generateW3(staff: any[], church: { name: string; ein: string; logo_url?: string }) {
    const { doc, churchName, ein } = baseDoc(church.name, church.ein);
    const employees = staff.filter((s: any) => s.type === 'Full-time' || s.type === 'Part-time');
    const totals = employees.reduce(
        (acc: any, s: any) => {
            const t = calcTaxes(s.salary * 12, true);
            return {
                gross: acc.gross + t.gross,
                ss: acc.ss + t.ss,
                medicare: acc.medicare + t.medicare,
                federal: acc.federal + t.federal,
            };
        },
        { gross: 0, ss: 0, medicare: 0, federal: 0 }
    );

    pageHeader(doc, 'Form W-3', 'Transmittal of Wage and Tax Statements', TAX_YEAR, church.logo_url);

    let y = 70;
    drawBox(doc, 40, y, 260, 32, 'b  Kind of Payer', 'Church / Religious Organization');
    drawBox(doc, 305, y, 260, 32, 'c  Total number of W-2 forms', `${employees.length}`);
    y += 40;
    drawBox(doc, 40, y, 260, 32, 'Employer EIN', ein);
    drawBox(doc, 305, y, 260, 32, 'Tax Year / Form Type', `${TAX_YEAR} / W-2`);
    y += 40;
    drawBox(doc, 40, y, 525, 32, 'Employer Name & Address', churchName);
    y += 46;

    doc.setFont('helvetica', 'bold'); doc.setFontSize(10); doc.setTextColor(10, 36, 90);
    doc.text('TOTALS FROM ALL W-2 FORMS', 40, y);
    y += 12;

    const boxes941 = [
        ['1  Wages, Tips, Other Compensation', fmt(totals.gross)],
        ['2  Federal Income Tax Withheld', fmt(totals.federal)],
        ['3  Social Security Wages', fmt(totals.gross)],
        ['4  Social Security Taxes Withheld', fmt(totals.ss)],
        ['5  Medicare Wages', fmt(totals.gross)],
        ['6  Medicare Tax Withheld', fmt(totals.medicare)],
    ];

    let col = 0;
    boxes941.forEach(([label, value], i) => {
        const x = 40 + (col % 2) * 265;
        if (i > 0 && i % 2 === 0) y += 46;
        drawBox(doc, x, y, 258, 40, label, value);
        col++;
    });

    y += 58;
    doc.setFillColor(240, 250, 245);
    doc.rect(40, y, 525, 55, 'F');
    doc.setDrawColor(16, 185, 129);
    doc.rect(40, y, 525, 55);
    doc.setFont('helvetica', 'bold'); doc.setFontSize(10); doc.setTextColor(8, 90, 50);
    doc.text('SUMMARY', 52, y + 17);
    doc.setFont('helvetica', 'normal'); doc.setFontSize(9); doc.setTextColor(20, 60, 30);
    doc.text(`Total employees: ${employees.length}   |   Total gross wages: ${fmt(totals.gross)}   |   Total withheld: ${fmt(totals.federal + totals.ss + totals.medicare)}`, 52, y + 34);

    footer(doc, 'W-3 must accompany all W-2 copies to SSA by January 31.');
    doc.save(`Form_W3_${TAX_YEAR}.pdf`);
}

// ── Form 990 Summary ─────────────────────────────────────────────────────────
export function generate990Summary(totals: { income: number; expenses: number; balance: number; members: number }, church: { name: string; ein: string; logo_url?: string }) {
    const { doc, churchName, ein } = baseDoc(church.name, church.ein);

    pageHeader(doc, 'Form 990 — Summary Worksheet', 'Return of Organization Exempt From Income Tax (Nonprofit)', TAX_YEAR, church.logo_url);

    let y = 70;
    drawBox(doc, 40, y, 350, 32, 'Organization Name', churchName);
    drawBox(doc, 395, y, 170, 32, 'EIN', ein);
    y += 40;
    drawBox(doc, 40, y, 525, 28, 'Tax Year', `January 1, ${TAX_YEAR} — December 31, ${TAX_YEAR}`);
    y += 38;
    drawBox(doc, 40, y, 525, 28, 'Organization Type', '501(c)(3) — Religious Organization');
    y += 42;

    doc.setFont('helvetica', 'bold'); doc.setFontSize(10); doc.setTextColor(10, 36, 90);
    doc.text('PART I — REVENUE, EXPENSES & CHANGES IN NET ASSETS', 40, y);
    y += 14;

    const netAssets = totals.income - totals.expenses;
    const lines990: [string, string][] = [
        ['Line 1   Total contributions (tithes, offerings, donations)', fmt(totals.income)],
        ['Line 8   Total Revenue', fmt(totals.income)],
        ['Line 13  Grants and similar amounts paid', '$0.00'],
        ['Line 16  Salaries, compensation, other employee benefits', fmt(totals.expenses * 0.7)],
        ['Line 17  Other expenses (facilities, programs, outreach)', fmt(totals.expenses * 0.3)],
        ['Line 18  Total Expenses', fmt(totals.expenses)],
        ['Line 19  Revenue Less Expenses', fmt(netAssets)],
        ['Line 22  Net Assets at End of Year', fmt(totals.balance)],
    ];

    lines990.forEach(([label, value]) => {
        const isExpense = label.includes('16') || label.includes('17') || label.includes('18');
        const isNet = label.includes('19') || label.includes('22');
        if (isExpense) { doc.setTextColor(180, 40, 40); }
        else if (isNet) { doc.setTextColor(10, 90, 40); }
        else { doc.setTextColor(0, 0, 0); }
        drawBox(doc, 40, y, 525, 36, label, value);
        doc.setTextColor(0, 0, 0);
        y += 42;
    });

    y += 6;
    doc.setFillColor(248, 250, 255);
    doc.rect(40, y, 525, 48, 'F');
    doc.setDrawColor(100, 100, 200);
    doc.rect(40, y, 525, 48);
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9); doc.setTextColor(30, 30, 90);
    doc.text('PART V — STATEMENTS REGARDING OTHER IRS FILINGS', 52, y + 14);
    doc.setFont('helvetica', 'normal'); doc.setFontSize(8); doc.setTextColor(60, 60, 100);
    doc.text(`Number of W-2s issued: See payroll records   |   Forms 1099-NEC filed: See contractor records   |   Members: ${totals.members}`, 52, y + 30);

    footer(doc, 'Complete Form 990 or 990-EZ at irs.gov by May 15. This worksheet helps prepare your return.');
    doc.save(`Form990_Summary_${TAX_YEAR}.pdf`);
}
